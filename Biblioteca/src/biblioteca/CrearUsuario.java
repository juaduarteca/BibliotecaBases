    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
     
        switch (boxtipo.getSelectedIndex())
        {
            case 0: 
                habilitarCampos();
                opera.ingreso(String.format("insert into profesor  values(%d,'%s','%s','%s');",Integer.parseInt(this.texcodigo.getText()),this.texnombre.getText(),this.texcuidad.getText(),this.texdireccion.getText()));
                limpiar();
                break;
            case 1: 
                habilitarCampos();
                opera.ingreso(String.format("insert into estudiante values(%d,'%s','%s','%s');",Integer.parseInt(this.texcodigo.getText()),this.texnombre.getText(),this.texcuidad.getText(),this.texdireccion.getText()));
                limpiar();
                break;
   
        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

    private void jToggleButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton2ActionPerformed
    texcodigo.setEditable(true);
    texnombre.setEnabled(false);
    texcuidad.setEnabled(false);
    texdireccion.setEnabled(false);
    switch(boxtipo.getSelectedIndex())
    {
        case 0:
            opera.ingreso(String.format("delete from profesor where codigop = %d;", Integer.parseInt(this.texcodigo.getText().toString())));
            limpiar();
            break;
        case 1:
            opera.ingreso(String.format("delete from estudiante where codigoe = %d;", Integer.parseInt(this.texcodigo.getText().toString())));
            limpiar();
            break;
        
    }
    
    
    opera.ingreso(String.format("delete from profesor where codigop = %d;", Integer.parseInt(this.texcodigo.getText().toString())));
    limpiar();
    }//GEN-LAST:event_jToggleButton2ActionPerformed

