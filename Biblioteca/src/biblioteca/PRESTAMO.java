    public  static String fechaActual()
    {
        Date fecha = new Date();
        SimpleDateFormat formatoFecha = new SimpleDateFormat("YYYY/MM/dd");
        
        return formatoFecha.format(fecha);   
    }
    public Date sumarFecha(Date fecha, int dias)
    {   
        Calendar calendar = Calendar.getInstance();
        calendar.setTime(fecha); // Configuramos la fecha que se recibe
        calendar.add(Calendar.DAY_OF_YEAR, dias);  // numero de días a añadir, o restar en caso de días<0
        return (Date) calendar.getTime();
    }
    public String aumentoFecha()
    {   
        Date fecha1 = new Date();
        fecha1 = sumarFecha(jDateChooser1.getDate(), 15);
        DateFormat fechaHora = new SimpleDateFormat("yyyy-MM-dd");
        String convertido = fechaHora.format(fecha1);
        return convertido;   
    }
    public  Date obtener()
    {   
        Date date = null;
        textFechaPrestamo.setText(fechaActual());
        SimpleDateFormat formatter = new SimpleDateFormat("dd-MMM-yyyy");
        String dateInString = textFechaPrestamo.getText();

        try {

            date = formatter.parse(dateInString);
            System.out.println(date);
            System.out.println(formatter.format(date));
            return date;

        } catch (ParseException e) {
            e.printStackTrace();
        }
        return date;
        
    }
    
